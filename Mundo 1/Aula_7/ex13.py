
# Faça um algoritmo que leia o salário de um funcionário e mostre seu novo salário 
# com 15% de aumento 

salario_antigo = float(input("Qual o seu salário ? "))

salario_novo = salario_antigo + (salario_antigo * 0.15)

print("Você acaba de receber um AUMENTO de salário !!!!!")
print(f"Seu novo salário é {salario_novo}")